
public class Customer {
	private String customer_name;
	private int customer_age;
	private int annual_income;
	private int credit_score;
	
	public String get_customer_name(String customer_name) {
		return customer_name = this.customer_name;
	}
	public int get_customer_age(int customer_age) {
		return customer_age = this.customer_age;
	}
	public int get_annual_income(int annual_income) {
		return annual_income = this.annual_income;
	}
	public int get_credit_score(int credit_score) {
		return credit_score = this.credit_score;
	}

}
