
public class Account {

	private String name;
	private int account_number;
	private int activate_days;
	private String gender;
	
	public String get_name(String name) {
		return name = this.name;
	}
	
	public int get_account_number(int account_number) {
		return account_number = this.account_number;
	}
	public int get_activate_days(int activate_days) {
		return activate_days = this.activate_days;
	}
	public String get_gender(String gender) {
		return gender = this.gender;
	}
}
